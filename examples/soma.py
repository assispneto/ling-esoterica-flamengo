def main():
    print("--- Calculadora do Mengão ---")

    print("Digite o primeiro número:")
    numero1 = int(input())

    print("Digite o segundo número:")
    numero2 = int(input())

    resultado = int(numero1 + numero2)

    print("O resultado da soma é:")
    print(resultado)
if __name__ == "__main__":
    main()
